/**
 * タグ配列から最初にマッチするカテゴリアイコンを返す。
 * マッチしない場合は 🥃。
 */
export function getTagIcon(tags: string[]): string {
  for (const tag of tags) {
    for (const [keyword, icon] of TAG_ICON_MAP) {
      if (tag.includes(keyword)) return icon
    }
  }
  return '🥃'
}
